<p>Which blood types are rare?</p>
A rare blood type is any blood type that is needed by patients and is difficult to find in the population.

<p>Why is closely-matched blood important?</p>
Some patients, particularly those who require on-going transfusions, need blood that closely matches their blood type.

This involves matching blood more extensively than the main blood types.

Some rare subtypes are more common in specific communities, which is why we particularly need more blood donors from black, Asian and minority ethnic communities.

<p>Why we need more black donors</p>
Growing demand for better-matched blood means we need 40,000 more black donors
Black, Asian and minority ethnic donors
Some blood conditions that are treated with transfusions most commonly affect people from black, Asian and minority ethnic communities

<p>How long does blood last after it’s been donated?</p>
Blood components have a limited shelf life:
<ul>
<li>red blood cells can be stored for up to 35 days</li>
<li>platelets can be stored for up to 7 days</li>
<li>plasma can be stored for up to 3 years</li>
<li>We only collect what we need to avoid wasting any donations.</li>
</ul>
