<p>Which blood types are compatible?</p>
You can receive blood from a donor with the same blood type as you.

You can also receive blood from a donor with a compatible blood type.

Similarly, a person with a different blood type to you may be able to safely receive your blood.

O negative blood is known as the universal blood type because it is safe for everyone to receive O negative red cells.

Who can receive my blood?

<p>How do blood types work?</p>
Your blood group is inherited from genes passed on by your parents.

The two most important blood group systems for transfusion are ABO and Rhesus (Rh).

The ABO group system gives the letter part of your blood type and Rhesus system gives the positive or negative part.

The ABO group system
There are four main blood groups in the ABO group system.

Each group is important and we need donors from every group to ensure we have the right blood for the people who need it.

Which group you belong to depends on the antigens and antibodies in your blood.

Antigens are a combination of sugars and proteins that coat the surface of a red blood cell.

Depending on your combination, you will have:
<ul>
<li>no antigens</li>
<li>A antigens</li>
<li>B antigens</li>
<li>both A and B antigens</li>
</ul>
You also have antibodies in the clear part of blood called plasma.

Antibodies are important in transfusion because they are the body’s natural defence system. They recognise any ‘foreign’ antigens and tell your immune system to destroy them.

That’s why giving someone blood from the wrong ABO group can be life-threatening.

It is the presence of A and B antigens and corresponding antibodies that determines your ABO blood group.

Blood group	Antigens on red cell	Antibodies in plasma
O	none	anti-A and anti-B
A	A	anti-B
B	B	anti-A
AB	A and B	none
Positive or negative blood
The D antigen in the Rhesus (Rh) system determines whether you have positive or negative blood.

If you have the Rh D antigen, your blood type is positive. If you lack the Rh D antigen, your blood type is negative.

77% of our donors are Rh D positive.

There are four other main antigens in the Rh system. Your combination of these makes up your blood subtype.
