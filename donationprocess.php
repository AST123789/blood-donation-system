<p>The donation process</p>
Giving blood is simple and it saves lives. When you give blood, it is collected so it can be used to treat someone else.

For most people, giving blood is easy and follows the simple steps listed below. See our animation which takes you through what happens step by step.

<p>Before you give blood</p>
<ul>
<li>Check you are able to give blood.</li>
<li>Register to be a donor online, or call 0300 123 23 23.</li>
<li>Sign in to your online account and find an appointment – a number of appointments can be booked in advance. If you are unable to book now, then please try for a later date. A limited number of walk-in slots are available.</li>
<li>Follow the preparing to give blood recommendations.</li>
<li>If you are unable to keep your donation appointment please try and give us at least 3 days' notice. You can easily cancel or reschedule your appointment when you sign in to your online account.</li>
<br><br>
<img src="donation.jpg"/>
</ul>
<p>When you give blood</p>
This is what will happen when you attend your appointment, or go to a drop-in session, to give blood. If you attend an appointment to give blood we aim to ensure it takes no more than an hour.
<br><br><img src="Blood-Donation-Process.png"/>
<p>1. Welcome and preparation</p>
Remember to bring your completed donor healthcheck form with you, if you received one from us in the post. We will ask you to read our welcome leaflet which explains the importance of blood safety. It’s important to read this whenever you attend because advice does change. We will give you 500ml of fluid just before you give blood. Drinking this over about 5 minutes will help with your well-being during and after donation.

<p>2. Health screening</p>
We make sure it is safe for you to donate, and that your blood donation is safe for a patient to receive.

We confirm your identity and ask you about the information on your donor health check form; a registered nurse may follow up if necessary.
We test a drop of blood from your finger to check the iron levels (haemoglobin) in your blood.
If you are not able to donate we will explain why, and may ask you to make another appointment.

If you are able to donate you will be asked to sit in a waiting area until you are called to a donation chair.

<p>3. Your blood donation</p>
When you are comfortable on the chair we will ask you to confirm your name, address and date of birth.

We examine your arm and place a cuff on your arm to maintain a small amount of pressure during donation (this does not measure blood pressure).

We then examine your arm to find a suitable vein and clean it with an antiseptic sponge.
<ul>
<li>We will insert a needle in your arm which will collect your blood into a blood bag with your unique donor number.</li>
<li>You should not feel any discomfort or pain. If you do, tell a member of staff.</li>
<li>A scale weighs the blood and stops when you have donated 470ml (or just under a pint). This usually takes between 5-10 minutes.</li>
<li>The needle will be removed and a sterile dressing applied to your arm.</li>
</ul>
